;	| IDE_REG_DATA		| 0x00	| R/W	| DATA INPUT/OUTPUT		|
;	| IDE_REG_ERR		| 0x01	| R	| ERROR REGISTER		|
;	| IDE_REG_FEAT		| 0x01	| W	| FEATURES REGISTER		|
