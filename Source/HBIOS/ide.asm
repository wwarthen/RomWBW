	CALL	PC_SPACE
	CALL	PRTHEXBYTE
#ENDIF
	LD	A,IDE_CMD_IDPKTDEV
	LD	(IDE_CMD),A
	CALL	IDE_RUNCMD_ND
	RET	NZ
;
	CALL	IDE_IN
	.DB	IDE_REG_STAT
	BIT	3,A			; IS DRQ SET?
	JP	Z,IDE_NOMEDIA
;
	LD	HL,HB_WRKBUF
	JP	IDE_GETBUF		; EXIT THRU BUFRD
;
;
;
IDE_PACKET:
#IF (IDETRACE >= 3)
	CALL	IDE_PRTPREFIX
	PRTS(" PACKET$")
#ENDIF
	LD	A,(IDE_DRVHD)
	;OUT	(IDE_REG_DRVHD),A
	CALL	IDE_OUT
	.DB	IDE_REG_DRVHD
#IF (IDETRACE >= 3)
	CALL	PC_SPACE
	CALL	PRTHEXBYTE
#ENDIF
	XOR	A			; ZERO
	CALL	IDE_OUT
	.DB	IDE_REG_FEAT		; FEATURE REG = 0
	CALL	IDE_OUT
	.DB	IDE_REG_BCL
	LD	A,8
	CALL	IDE_OUT
	.DB	IDE_REG_BCH		; BYTE COUNT = 512????
	LD	A,IDE_CMD_PACKET
	LD	(IDE_CMD),A
	JP	IDE_RUNCMD_ND
;
;
;
IDE_RDSEC:
;
#IF (IDETRACE >= 3)
	CALL	IDE_PRTPREFIX
	PRTS(" READ$")
#ENDIF
	LD	A,(IDE_DRVHD)
	;OUT	(IDE_REG_DRVHD),A
	CALL	IDE_OUT
	.DB	IDE_REG_DRVHD
#IF (IDETRACE >= 3)
	CALL	PC_SPACE
	CALL	PRTHEXBYTE
#ENDIF
	LD	A,(IY+IDE_TYPE)
	CP	IDE_TYPEATA
	JR	Z,IDE_ATA_RDSEC
	CP	IDE_TYPEATAPI
	JR	Z,IDE_PKT_RDSEC
	JP	IDE_CMDERR
;
IDE_ATA_RDSEC:
#IF (IDETRACE >= 3)
	PRTS(" ATA$")
#ENDIF
	CALL	IDE_SETADDR		; SETUP CYL, TRK, HEAD
	LD	A,IDE_CMD_READ
	LD	(IDE_CMD),A
	CALL	IDE_RUNCMD
	RET	NZ
	LD	HL,(IDE_DSKBUF)
	JP	IDE_GETBUF
;
IDE_PKT_RDSEC:
#IF (IDETRACE >= 3)
	PRTS(" PKT$")
#ENDIF
	; SETUP LBA
;
;;;#IF (DSKYENABLE)
;;;  #IF (DSKYDSKACT)
	LD	A,IDE_LBA
	CALL	LDHLIYA
	CALL	HB_DSKACT		; SHOW ACTIVITY
;;;  #ENDIF
;;;#ENDIF
;
	; 3 BYTES, LITTLE ENDIAN -> BIG ENDIAN
	LD	HL,IDE_PKTCMD_RW10+3	; START OF LBA FIELD IN CDB (MSB)
	LD	A,(IY+IDE_LBA+2)	; THIRD BYTE OF LBA FIELD IN CFG (MSB)
	LD	(HL),A
	INC	HL
	LD	A,(IY+IDE_LBA+1)
	LD	(HL),A
	INC	HL
	LD	A,(IY+IDE_LBA+0)
	LD	(HL),A
	INC	HL
	LD	HL,IDE_PKTCMD_RW10
	LD	A,SCSI_CMD_READ10
	LD	(HL),A
	XOR	A			; READ DIRECTION
	LD	(IDE_XFRDIR),A		; SAVE IT
	CALL	IDE_RUNPCMD
	JP	NZ,IDE_CHKPCMD
	RET
;
;
;
IDE_WRSEC:
;
#IF (IDETRACE >= 3)
	CALL	IDE_PRTPREFIX
	PRTS(" WRITE$")
