#include "cfg_mbc.asm"
;
CPUOSC		.EQU	4000000		; CPU OSC FREQ IN MHZ
;
BATCOND		.SET	FALSE
;
PPIDEENABLE	.SET	TRUE		; PPIDE: ENABLE PARALLEL PORT IDE DISK DRIVER (PPIDE.ASM)
;
DSKYENABLE	.SET	TRUE		; ENABLES DSKY
DSKYMODE	.SET	DSKYMODE_NG	; DSKY VERTSION: DSKYMODE_[V1|NG]
