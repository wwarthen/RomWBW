;	CALL	XXXX			; CALL INT HANDLER
;	RET	NZ			; RETURN IF HANDLED
;
HB_IM1INT:	; IM1 DEVICE INTERRUPT HANDLER
	JP	HB_BADINT
	RET				; START WITH NO ENTRIES
