; SCSI DEVICE STATUS CODES
;
SCSI_STOK	.EQU	0
SCSI_STNOMEDIA	.EQU	-1
SCSI_STIOERR	.EQU	-2
SCSI_STTO	.EQU	-3
SCSI_STNOTRDY	.EQU	-4
;
; NCR 5380 I/O REGISTERS
;
SCSI_SR_CSD	.EQU	$00		; CURRENT SCSI DATA
SCSI_SR_ICR	.EQU	$01		; INITIATOR COMMAND
SCSI_SR_MR	.EQU	$02		; MODE
SCSI_SR_TCR	.EQU	$03		; TARGET COMMAND
SCSI_SR_CSBS	.EQU	$04		; CURRENT SCSI BUS STAT
SCSI_SR_BSR	.EQU	$05		; BUS STATUS
SCSI_SR_IDR	.EQU	$06		; INITIATOR DMA RECV
SCSI_SR_RPI	.EQU	$07		; RESET PARITY/IRQ
;
; NCR 5380 OUTPUT ONLY REGISTERS
;
SCSI_SR_ODR	.EQU	$00		; OUTPUT DATA
SCSI_SR_SER	.EQU	$04		; SELECT ENABLE
SCSI_SR_SDS	.EQU	$05		; START DMA SEND
SCSI_SR_SDTR	.EQU	$06		; START TARGET DMA RECV
SCSI_SR_SDIR	.EQU	$07		; START INITIATOR DMA RECV
;
#IF (SCSIMODE == SCSIMODE_LATCH)
SCSI_REGSEL	.EQU	SCSIBASE	; SCSI CHIP REGISTER SELECTION
SCSI_PDMA	.EQU	SCSIBASE + $01	; SCSI PSEUDO-DMA I/O
SCSI_DATA	.EQU	SCSIBASE + $02	; SCSI CHIP I/O
SCSI_CTL	.EQU	SCSIBASE + $03	; SCSI CONTROL & STATUS
#ENDIF
;
; SCSI 5380 CHIP REGISTER ACCESS MACROS
;
#IF (SCSIMODE == SCSIMODE_LATCH)
;
#DEFINE SCSI_OUT(REG)	PUSH	AF
    #DEFCONT \		LD	A,REG
    #DEFCONT \		OUT	(SCSI_REGSEL),A
    #DEFCONT \		POP	AF
    #DEFCONT \		OUT	(SCSI_DATA),A
;
#DEFINE SCSI_IN(REG)	LD	A,REG
    #DEFCONT \		OUT	(SCSI_REGSEL),A
    #DEFCONT \		IN	A,(SCSI_DATA)
;
#ENDIF
;
#IF (SCSIMODE == SCSIMODE_DIRECT)
;
#DEFINE SCSI_IN(REG)	IN	A,(SCSIBASE + REG)
#DEFINE SCSI_OUT(REG)	OUT	(SCSIBASE + REG),A
;
#ENDIF
;
; SCSI CURRENT BUS STATUS BIT MASKS
;
SCSI_SM_RST	.EQU	$80		; RESET
SCSI_SM_BSY	.EQU	$40		; BUSY
SCSI_SM_REQ	.EQU	$20		; REQUEST
SCSI_SM_MSG	.EQU	$10		; MESSAGE
SCSI_SM_CD	.EQU	$08		; CMD/DATA
SCSI_SM_IO	.EQU	$04		; IN/OUT
SCSI_SM_SEL	.EQU	$02		; SELECT
SCSI_SM_DBP	.EQU	$01		; PARITY BIT
;
SCSI_SM_PHM	.EQU	$08		; PHASE MATCH BIT
;
; SDCI DEVICE CONFIGURATION
;
; PER DEVICE DATA OFFSETS IN CFG BLOCK
;
SCSI_DEV	.EQU	0		; OFFSET OF DEVICE NUMBER (BYTE)
SCSI_IOBASE	.EQU	1		; IO BASE ADDRESS (BYTE)
SCSI_TGT	.EQU	2		; TARGET DEVICE ID
SCSI_TID	.EQU	3		; TARGET DEVICE BIT PATTERN
SCSI_LUN	.EQU	4		; TARGET LUN
SCSI_STAT	.EQU	5		; LAST STATUS (BYTE)
SCSI_MEDCAP	.EQU	6		; MEDIA CAPACITY (DWORD)
SCSI_LBA	.EQU	10		; OFFSET OF LBA (DWORD)
;
SCSI_CFGSIZ	.EQU	14		; SIZE OF CFG TBL ENTRIES
;
;--------------------------------------------------------------------------------------------------
;   HBIOS MODULE HEADER
;--------------------------------------------------------------------------------------------------
;
ORG_SCSI	.EQU	$
;
	.DW	SIZ_SCSI		; MODULE SIZE
	.DW	SCSI_INITPHASE		; ADR OF INIT PHASE HANDLER
;
SCSI_INITPHASE:
	; INIT PHASE HANDLER, A=PHASE
	;CP	HB_PHASE_PREINIT	; PREINIT PHASE?
	;JP	Z,SCSI_PREINIT		; DO PREINIT
	CP	HB_PHASE_INIT		; INIT PHASE?
	JP	Z,SCSI_INIT		; DO INIT
	RET				; DONE
;
SCSI_CFGTBL:
;
#IF (SCSICNT >= 1)
;
SCSI0_CFG:
	.DB	$FE			; DRIVER DEVICE NUMBER (FILLED DYNAMICALLY)
	.DB	SCSIBASE		; IO BASE ADDRESS
	.DB	SCSI0_TGT		; TARGT DEVICE ID
	.DB	1 << SCSI0_TGT		; TARGET DEVICE BIT PATTERN
	.DB	SCSI0_LUN		; SCSI TARGET LUN
	.DB	SCSI_STNOTRDY		; DEVICE STATUS
	.DW	$0000,$0010		; DEVICE CAPACITY (BLOCKS)
	.DW	0,0			; CURRENT LBA
;
	DEVECHO	"SCSI: IO="
	DEVECHO	SCSIBASE
	DEVECHO ", TGT="
	DEVECHO SCSI0_TGT
	DEVECHO ", LUN="
	DEVECHO SCSI0_LUN
	DEVECHO ", MODE="
  #IF (SCSIMODE == SCSIMODE_DIRECT)
	DEVECHO	"DIRECT"
  #ENDIF
  #IF (SCSIMODE == SCSIMODE_LATCH)
	DEVECHO	"LATCH"
  #ENDIF
  #IF (SCSIPDMA)
	DEVECHO	", PDMA"
  #ENDIF
	DEVECHO	"\n"
#ENDIF
;
#IF (SCSICNT >= 2)
	.DB	$FE			; DRIVER DEVICE NUMBER (FILLED DYNAMICALLY)
	.DB	SCSIBASE		; IO BASE ADDRESS
	.DB	SCSI1_TGT		; TARGT DEVICE ID
	.DB	1 << SCSI1_TGT		; TARGET DEVICE BIT PATTERN
	.DB	SCSI1_LUN		; SCSI TARGET LUN
	.DB	SCSI_STNOTRDY		; DEVICE STATUS
	.DW	$0000,$0010		; DEVICE CAPACITY (BLOCKS)
	.DW	0,0			; CURRENT LBA
;
	DEVECHO	"SCSI: IO="
