  #ENDIF
	DEVECHO	"\n"
#ENDIF
;
#IF (SCSICNT >= 3)
	.DB	$FE			; DRIVER DEVICE NUMBER (FILLED DYNAMICALLY)
	.DB	SCSIBASE		; IO BASE ADDRESS
	.DB	SCSI2_TGT		; TARGT DEVICE ID
	.DB	1 << SCSI2_TGT		; TARGET DEVICE BIT PATTERN
	.DB	SCSI2_LUN		; SCSI TARGET LUN
